    public boolean  Insertar(int x){
       
        int tam=5;
        if(tope==tam-1){
            return false;
        }else{
            tope++;
            data[tope]=x;
            return true;
        }
    }
    
    public boolean Eliminar(){
        eliminado=0;
        if(tope==-1){
            return false;
        }else{
            eliminado=data[tope];
            tope--;
            return true;
        }
    }


